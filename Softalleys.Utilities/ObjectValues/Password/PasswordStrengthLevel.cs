namespace Softalleys.Utilities.ObjectValues.Password;

/// <summary>
/// Represents the strength levels that a password can have.
/// </summary>
public enum PasswordStrengthLevel
{
    /// <summary>
    ///     The password is weak.
    /// </summary>
    Weak = 0,

    /// <summary>
    ///     The password is moderate.
    /// </summary>
    Moderate = 1,

    /// <summary>
    ///     The password is strong.
    /// </summary>
    Strong = 2
}